package ss01_introduction_to_java.practice;



public class KhaiBaoBien2 {
    public static void main(String[] args) {
        byte i = 10;
        System.out.println("Biến i có kiểu dữ liệu số nguyên = " + i);
        double f = 20.5;
        System.out.println("Biến f có kiểu dữ liệu số thực = " + f);
        boolean b = true;
        System.out.println("Biến b có kiểu dữ liệu boolean = " + b);
        char c = 'a';
        System.out.println("Biến c có kiểu dữ liệu char = " + c);
        String s = "Đà Nẵng";
        System.out.println("Biến s có kiểu dữ liệu chuỗi = " + s);

    }
}
